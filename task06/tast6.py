list = ['камень', 'ножницы', 'бумага' ]
print ('Начнем игру.')

def fun():    
    while True:
         igr = getpass.getpass(' Введите: камень, ножницы, бумага - ')
         igr = igr.lower() 
         if igr not in list:
            print("Вам нужно выбрать из вариантов: " + ','.join(list))
         else:
            break
    return igr

while True:
     igr1 = fun()
     igr2 = fun()
     
     if igr1 == igr2:
        print('Ничья. Игра продолжается до ПОБЕДЫ! 1-й игрок выбрал:',igr1,'\n2-й игрок выбрал:', igr2)      
     else:
         print('1-й игрок выбрал:',igr1,'\n2-й игрок выбрал:', igr2,'\nПобедил', res[(igr1, igr2)])
         break
   

 





    

   
    

